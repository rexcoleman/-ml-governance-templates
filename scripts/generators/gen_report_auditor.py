class Finding:
    def __init__(self, check: str, severity: str, message: str, line: int = 0):
        self.check = check
        self.severity = severity
        self.message = message
        self.line = line
        self.passed = severity == "PASS"

    def __repr__(self):
        loc = f" (line {self.line})" if self.line else ""
        return f"[{self.severity}] {self.check}{loc}: {self.message}"


def check_title(text: str, latex: bool) -> list[Finding]:
    """Check 1: Title is declarative, not procedural."""
    findings = []
    title = extract_title_latex(text) if latex else extract_title_md(text)
    if title is None:
        findings.append(Finding("title_check", MEDIUM, "Could not extract title from report"))
        return findings

    for prefix in REJECTED_TITLE_PREFIXES:
        if title.lower().startswith(prefix.lower()):
            findings.append(Finding(
                "title_check", HIGH,
                f"Title starts with rejected pattern \"{prefix}\": \"{title}\""
            ))
            return findings

    findings.append(Finding("title_check", "PASS", f"Title is declarative: \"{title}\""))
    return findings


def check_abstract_word_count(text: str, latex: bool) -> list[Finding]:
    """Check 2: Abstract word count within limit."""
    findings = []
    abstract = extract_abstract_latex(text) if latex else extract_abstract_md(text)
    if abstract is None:
        findings.append(Finding("abstract_word_count", MEDIUM, "Could not extract abstract"))
        return findings

    # Strip LaTeX commands for word counting
    clean = re.sub(r"\\[a-zA-Z]+\{[^{}]*\}", " ", abstract)
    clean = re.sub(r"\\[a-zA-Z]+", " ", clean)
    clean = re.sub(r"[{}\\$]", " ", clean)
    words = clean.split()
    count = len(words)

    if count > ABSTRACT_WORD_LIMIT:
        findings.append(Finding(
            "abstract_word_count", HIGH,
            f"Abstract has {count} words (limit: {ABSTRACT_WORD_LIMIT})"
        ))
    else:
        findings.append(Finding(
            "abstract_word_count", "PASS",
            f"Abstract has {count} words (limit: {ABSTRACT_WORD_LIMIT})"
        ))
    return findings


def check_paragraph_length(text: str) -> list[Finding]:
    """Check 3: No paragraph exceeds max sentence count."""
    findings = []
    paragraphs = get_paragraphs(text)
    violations = 0
    for line_num, para in paragraphs:
        if len(para.split()) < 10:
            continue  # skip very short paragraphs
        scount = count_sentences(para)
        if scount > MAX_PARAGRAPH_SENTENCES:
            violations += 1
            snippet = para[:100] + "..." if len(para) > 100 else para
            findings.append(Finding(
                "paragraph_length", MEDIUM,
                f"Paragraph has {scount} sentences (max: {MAX_PARAGRAPH_SENTENCES}): "
                f"\"{snippet}\"",
                line=line_num,
            ))
    if violations == 0:
        findings.append(Finding(
            "paragraph_length", "PASS",
            f"All paragraphs within {MAX_PARAGRAPH_SENTENCES}-sentence limit"
        ))
    return findings


def check_jargon_first_use(text: str) -> list[Finding]:
    """Check 4: Each jargon term is defined on first occurrence."""
    findings = []
    if not JARGON_TERMS:
        findings.append(Finding("jargon_first_use", "PASS", "No jargon terms configured"))
        return findings

    lines = text.splitlines()

    for entry in JARGON_TERMS:
        term = entry["term"]
        definition = entry.get("definition", "")
        if not term:
            continue

        term_lower = term.lower()

        # Find first occurrence line
        first_line = None
        first_line_num = 0
        for i, line in enumerate(lines, start=1):
            if term_lower in line.lower():
                first_line = line
                first_line_num = i
                break

        if first_line is None:
            findings.append(Finding(
                "jargon_first_use", LOW,
                f"Jargon term \"{term}\" not found in report",
            ))
            continue

        # Check if definition appears near first use (within ~200 chars)
        text_lower = text.lower()
        idx = text_lower.find(term_lower)
        if idx == -1:
            continue

        context_start = max(0, idx - 50)
        context_end = min(len(text), idx + len(term) + 200)
        context = text[context_start:context_end].lower()

        # Look for definition text, parenthetical, or common definition patterns
        has_definition = False
        if definition:
            has_definition = definition.lower() in context
        # Also accept parenthetical glosses: "term (definition)" or "term — definition"
        paren_pattern = re.compile(
            re.escape(term_lower) + r"\s*[\(\[]",
            re.IGNORECASE,
        )
        dash_pattern = re.compile(
            re.escape(term_lower) + r"\s*[—–-]\s*\w",
            re.IGNORECASE,
        )
        if paren_pattern.search(context) or dash_pattern.search(context):
            has_definition = True

        if has_definition:
            findings.append(Finding(
                "jargon_first_use", "PASS",
                f"\"{term}\" defined on first use (line {first_line_num})",
                line=first_line_num,
            ))
        else:
            findings.append(Finding(
                "jargon_first_use", MEDIUM,
                f"\"{term}\" used at line {first_line_num} without nearby definition",
                line=first_line_num,
            ))
    return findings


def check_terminology_consistency(text: str) -> list[Finding]:
    """Check 5: Rejected term variants are not used."""
    findings = []
    if not TERMINOLOGY_LOCK:
        findings.append(Finding("terminology_consistency", "PASS", "No terminology lock configured"))
        return findings

    lines = text.splitlines()
    found_violations = False

    for entry in TERMINOLOGY_LOCK:
        canonical = entry["canonical"]
        rejected = entry.get("rejected", [])
        for variant in rejected:
            if not variant:
                continue
            # Search for the rejected variant (case-insensitive, whole word)
            pattern = re.compile(r"\b" + re.escape(variant) + r"\b", re.IGNORECASE)
            for i, line in enumerate(lines, start=1):
                if pattern.search(line):
                    found_violations = True
                    findings.append(Finding(
                        "terminology_consistency", HIGH,
                        f"Rejected variant \"{variant}\" found (use \"{canonical}\" instead)",
                        line=i,
                    ))

    if not found_violations:
        findings.append(Finding(
            "terminology_consistency", "PASS",
            "All terminology consistent with canonical terms"
        ))
    return findings


def check_section_headers(text: str, latex: bool) -> list[Finding]:
    """Check 6: Section headers are declarative (not gerunds/bare method names)."""
    findings = []
    lines = text.splitlines()
    violations = 0

    for i, line in enumerate(lines, start=1):
        stripped = line.strip()
        # Detect headers
        is_header = False
        if latex:
            if re.match(r"\\(?:sub)*section\{", stripped):
                is_header = True
        else:
            if re.match(r"^#{1,4}\s+", stripped):
                is_header = True

        if not is_header:
            continue

        if GERUND_HEADER_RE.match(stripped):
            violations += 1
            findings.append(Finding(
                "section_headers", MEDIUM,
                f"Header starts with gerund: \"{stripped.strip()}\"",
                line=i,
            ))
        elif BARE_METHOD_RE.match(stripped):
            violations += 1
            findings.append(Finding(
                "section_headers", MEDIUM,
                f"Header is bare method name without claim: \"{stripped.strip()}\"",
                line=i,
            ))

    if violations == 0:
        findings.append(Finding(
            "section_headers", "PASS",
            "All section headers are declarative"
        ))
    return findings


def check_figure_table_crossrefs(text: str, latex: bool) -> list[Finding]:
    """Check 7: All \\ref{fig:X}/\\ref{tab:X} have matching \\label{} and vice versa."""
    findings = []

    if not latex:
        # For Markdown, skip this check (no \ref/\label convention)
        findings.append(Finding(
            "figure_table_crossrefs", "PASS",
            "Markdown report — LaTeX cross-ref check skipped"
        ))
        return findings

    # Collect \label{fig:X} and \label{tab:X}
    labels_fig = set(re.findall(r"\\label\{(fig:[^{}]+)\}", text))
    labels_tab = set(re.findall(r"\\label\{(tab:[^{}]+)\}", text))

    # Collect \ref{fig:X} and \ref{tab:X}
    refs_fig = set(re.findall(r"\\ref\{(fig:[^{}]+)\}", text))
    refs_tab = set(re.findall(r"\\ref\{(tab:[^{}]+)\}", text))

    all_labels = labels_fig | labels_tab
    all_refs = refs_fig | refs_tab

    # Orphan refs (referenced but never labeled)
    orphan_refs = all_refs - all_labels
    for ref in sorted(orphan_refs):
        findings.append(Finding(
            "figure_table_crossrefs", CRITICAL,
            f"\\ref{{{ref}}} referenced but no \\label{{{ref}}} found"
        ))

    # Orphan labels (labeled but never referenced)
    orphan_labels = all_labels - all_refs
    for label in sorted(orphan_labels):
        findings.append(Finding(
            "figure_table_crossrefs", HIGH,
            f"\\label{{{label}}} defined but never referenced with \\ref{{}}"
        ))

    if not orphan_refs and not orphan_labels:
        findings.append(Finding(
            "figure_table_crossrefs", "PASS",
            f"All {len(all_labels)} labels referenced, all {len(all_refs)} refs resolved"
        ))
    return findings


def check_citation_completeness(text: str, latex: bool) -> list[Finding]:
    """Check 8: All \\cite{X} have matching bibliography entries and vice versa."""
    findings = []

    if not latex:
        findings.append(Finding(
            "citation_completeness", "PASS",
            "Markdown report — LaTeX citation check skipped"
        ))
        return findings

    # Collect \cite{key1,key2,...}
    cited_keys: set[str] = set()
    for m in re.finditer(r"\\cite[tp]*\{([^{}]+)\}", text):
        for key in m.group(1).split(","):
            cited_keys.add(key.strip())

    # Collect \bibitem{key}
    bib_keys: set[str] = set()
    for m in re.finditer(r"\\bibitem\{([^{}]+)\}", text):
        bib_keys.add(m.group(1).strip())

    # Check for .bib file usage
    uses_bibtex = bool(re.search(r"\\bibliography\{", text))

    if not cited_keys:
        findings.append(Finding(
            "citation_completeness", LOW,
            "No \\cite{} commands found in report"
        ))
        return findings

    if uses_bibtex and not bib_keys:
        # Using bibtex — we cannot fully verify without parsing .bib file
        findings.append(Finding(
            "citation_completeness", LOW,
            f"Report uses \\bibliography{{}}; {len(cited_keys)} cite keys found. "
            "Cannot verify against .bib file — run bibtex for full check."
        ))
        return findings

    if bib_keys:
        orphan_cites = cited_keys - bib_keys
        orphan_bibs = bib_keys - cited_keys

        for key in sorted(orphan_cites):
            findings.append(Finding(
                "citation_completeness", CRITICAL,
                f"\\cite{{{key}}} has no matching \\bibitem{{}}"
            ))

        for key in sorted(orphan_bibs):
            findings.append(Finding(
                "citation_completeness", MEDIUM,
                f"\\bibitem{{{key}}} defined but never cited"
            ))

        if not orphan_cites and not orphan_bibs:
            findings.append(Finding(
                "citation_completeness", "PASS",
                f"All {len(cited_keys)} citations resolved, "
                f"all {len(bib_keys)} bibliography entries cited"
            ))
    else:
        findings.append(Finding(
            "citation_completeness", LOW,
            f"{len(cited_keys)} \\cite{{}} keys found but no \\bibitem{{}} entries "
            "(may use external .bib file)"
        ))

    return findings


def check_no_new_results_in_discussion(text: str, latex: bool) -> list[Finding]:
    """Check 9: No numeric values in Discussion that do not appear in Results."""
    findings = []

    sections = get_sections_latex(text) if latex else get_sections_md(text)

    # Find Results and Discussion sections (case-insensitive match)
    results_text = ""
    discussion_text = ""
    for name, body in sections.items():
        name_lower = name.lower().strip()
        if "result" in name_lower and "discussion" not in name_lower:
            results_text += body + "\n"
        if "discussion" in name_lower:
            discussion_text += body + "\n"

    if not discussion_text:
        findings.append(Finding(
            "no_new_results_discussion", "PASS",
            "No Discussion section found (or empty) — check skipped"
        ))
        return findings

    results_numbers = extract_numbers_from_text(results_text)
    discussion_numbers = extract_numbers_from_text(discussion_text)

    # Filter out very common numbers (0, 1, 2, etc.)
    trivial = {"0", "1", "2", "3", "4", "5", "10", "100"}
    new_in_discussion = discussion_numbers - results_numbers - trivial

    if new_in_discussion:
        sample = sorted(new_in_discussion)[:10]
        findings.append(Finding(
            "no_new_results_discussion", HIGH,
            f"{len(new_in_discussion)} numeric value(s) in Discussion not in Results: "
            f"{', '.join(sample)}"
        ))
    else:
        findings.append(Finding(
            "no_new_results_discussion", "PASS",
            "No new numeric values introduced in Discussion"
        ))
    return findings


def check_caption_quality(text: str, latex: bool) -> list[Finding]:
    """Check 10: Captions contain interpretation, not just 'Figure N' or 'Table N'."""
    findings = []

    if latex:
        # \caption{...}
        captions = re.findall(r"\\caption\{([^{}]+)\}", text)
    else:
        # Markdown: lines starting with "Figure N" or "Table N" after images
        captions = re.findall(
            r"(?:^|\n)\s*(?:Figure|Table)\s+\d+[.:]?\s*(.*)",
            text,
        )

    if not captions:
        findings.append(Finding(
            "caption_quality", "PASS",
            "No captions found in report"
        ))
        return findings

    weak_caption_re = re.compile(
        r"^\s*(?:Figure|Table)\s+\d+[.:]?\s*$",
        re.IGNORECASE,
    )
    # Also flag captions that are just "Figure N shows X" without interpretation
    shows_only_re = re.compile(
        r"^\s*(?:Figure|Table)\s+\d+\s+(?:shows|displays|presents|illustrates)\b",
        re.IGNORECASE,
    )

    weak_count = 0
    for cap in captions:
        cap_stripped = cap.strip()
        if weak_caption_re.match(cap_stripped):
            weak_count += 1
            findings.append(Finding(
                "caption_quality", MEDIUM,
                f"Caption lacks interpretation: \"{cap_stripped[:80]}\"",
            ))
        elif shows_only_re.match(cap_stripped) and len(cap_stripped.split()) < 8:
            weak_count += 1
            findings.append(Finding(
                "caption_quality", LOW,
                f"Caption may lack interpretation (very short): \"{cap_stripped[:80]}\"",
            ))

    if weak_count == 0:
        findings.append(Finding(
            "caption_quality", "PASS",
            f"All {len(captions)} captions contain interpretive content"
        ))
    return findings


def check_build(report_path: Path) -> list[Finding]:
    """Check 11: If .tex, attempt pdflatex compilation."""
    findings = []

    if not is_latex(report_path):
        findings.append(Finding(
            "build_check", "PASS",
            "Non-LaTeX report — build check skipped"
        ))
        return findings

    # Check if pdflatex is available
    try:
        subprocess.run(
            ["pdflatex", "--version"],
            capture_output=True, timeout=10,
        )
    except (FileNotFoundError, subprocess.TimeoutExpired):
        findings.append(Finding(
            "build_check", LOW,
            "pdflatex not found — build check skipped"
        ))
        return findings

    # Run pdflatex in nonstopmode
    try:
        result = subprocess.run(
            [
                "pdflatex",
                "-interaction=nonstopmode",
                "-halt-on-error",
                str(report_path.name),
            ],
            capture_output=True,
            text=True,
            timeout=120,
            cwd=str(report_path.parent),
        )

        if result.returncode != 0:
            # Extract first error
            error_lines = []
            for line in result.stdout.splitlines():
                if line.startswith("!"):
                    error_lines.append(line)
                    if len(error_lines) >= 3:
                        break

            error_msg = "; ".join(error_lines) if error_lines else "pdflatex exited with error"
            findings.append(Finding(
                "build_check", CRITICAL,
                f"pdflatex compilation failed: {error_msg}"
            ))
        else:
            # Check for warnings
            warnings_list = []
            overfull_count = 0
            for line in result.stdout.splitlines():
                if "Overfull" in line:
                    # Extract point value if present
                    m = re.search(r"(\d+\.?\d*)pt", line)
                    if m and float(m.group(1)) > OVERFULL_THRESHOLD:
                        overfull_count += 1
                elif "Warning" in line and "Font" not in line:
                    warnings_list.append(line.strip()[:120])

            if overfull_count > 0:
                findings.append(Finding(
                    "build_check", MEDIUM,
                    f"{overfull_count} overfull hbox warnings exceed {OVERFULL_THRESHOLD}pt threshold"
                ))

            if warnings_list:
                findings.append(Finding(
                    "build_check", LOW,
                    f"{len(warnings_list)} LaTeX warnings: {warnings_list[0]}"
                ))

            if overfull_count == 0 and not warnings_list:
                findings.append(Finding(
                    "build_check", "PASS",
                    "pdflatex compilation succeeded with no significant warnings"
                ))
            elif overfull_count == 0:
                findings.append(Finding(
                    "build_check", "PASS",
                    "pdflatex compilation succeeded"
                ))

    except subprocess.TimeoutExpired:
        findings.append(Finding(
            "build_check", HIGH,
            "pdflatex compilation timed out (>120s)"
        ))

    return findings


def check_page_count(report_path: Path) -> list[Finding]:
    """Check 12: PDF page count within limit."""
    findings = []

    # Look for a PDF with the same stem
    pdf_path = report_path.with_suffix(".pdf")
    if not pdf_path.exists():
        # Also check in parent directory
        for candidate in report_path.parent.glob("*.pdf"):
            pdf_path = candidate
            break

    if not pdf_path.exists():
        findings.append(Finding(
            "page_count", LOW,
            f"No PDF found at {pdf_path} — page count check skipped"
        ))
        return findings

    # Try to get page count
    page_count = None

    # Method 1: pdfinfo
    try:
        result = subprocess.run(
            ["pdfinfo", str(pdf_path)],
            capture_output=True, text=True, timeout=10,
        )
        for line in result.stdout.splitlines():
            if line.startswith("Pages:"):
                page_count = int(line.split(":")[1].strip())
                break
    except (FileNotFoundError, subprocess.TimeoutExpired, ValueError):
        pass

    # Method 2: python parsing (count /Type /Page)
    if page_count is None:
        try:
            raw = pdf_path.read_bytes()
            page_count = len(re.findall(rb"/Type\s*/Page(?!s)", raw))
        except Exception:
            pass

    if page_count is None:
        findings.append(Finding(
            "page_count", LOW,
            "Could not determine PDF page count"
        ))
        return findings

    if page_count > PAGE_LIMIT:
        findings.append(Finding(
            "page_count", HIGH,
            f"PDF has {page_count} pages (limit: {PAGE_LIMIT})"
        ))
    else:
        findings.append(Finding(
            "page_count", "PASS",
            f"PDF has {page_count} pages (limit: {PAGE_LIMIT})"
        ))
    return findings


def check_url_escaping(text: str, latex: bool) -> list[Finding]:
    """Check 13: No unescaped # in \\url{} or \\href{}."""
    findings = []

    if not latex:
        findings.append(Finding(
            "url_escaping", "PASS",
            "Non-LaTeX report — URL escaping check skipped"
        ))
        return findings

    lines = text.splitlines()
    violations = 0

    for i, line in enumerate(lines, start=1):
        # Find \url{...} and \href{...}
        for m in re.finditer(r"\\(?:url|href)\{([^{}]+)\}", line):
            url = m.group(1)
            # Check for unescaped # (not preceded by \)
            for j, ch in enumerate(url):
                if ch == "#" and (j == 0 or url[j - 1] != "\\"):
                    violations += 1
                    findings.append(Finding(
                        "url_escaping", HIGH,
                        f"Unescaped # in URL: \"{url[:80]}\"",
                        line=i,
                    ))
                    break  # one finding per URL

    if violations == 0:
        findings.append(Finding(
            "url_escaping", "PASS",
            "All URLs properly escaped"
        ))
    return findings


