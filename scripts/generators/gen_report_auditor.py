#!/usr/bin/env python3
"""gen_report_auditor.py — G13: Generate report consistency auditor from project.yaml.

Reads the audit section from project.yaml and produces a scripts/audit_report.py
script that performs machine-verifiable checks from the REPORT_CONSISTENCY_SPEC
template: title quality, abstract word count, paragraph length, jargon first-use,
terminology consistency, declarative headers, figure/table cross-refs, citation
completeness, no-new-results-in-Discussion, caption quality, build check, page
count, and URL escaping.

Usage:
    python scripts/generators/gen_report_auditor.py project.yaml [--output scripts/audit_report.py]
"""
import argparse
from pathlib import Path

import yaml


def load_project(path: str) -> dict:
    with open(path) as f:
        return yaml.safe_load(f)


def generate_report_auditor(project: dict, output_path: str) -> str:
    """Generate audit_report.py content from project.yaml."""
    audit = project.get("audit", {})
    project_name = project.get("project", {}).get("name", "Experiment")

    tsr = audit.get("ten_simple_rules", {})
    abstract_word_limit = tsr.get("abstract_word_limit", 250)
    max_paragraph_sentences = tsr.get("max_paragraph_sentences", 8)

    # Jargon terms — list of dicts with term/definition or just strings
    jargon_terms_raw = tsr.get("jargon_terms", [])
    jargon_lines = []
    for entry in jargon_terms_raw:
        if isinstance(entry, dict):
            term = entry.get("term", "")
            definition = entry.get("definition", "")
            jargon_lines.append(f'        {{"term": {repr(term)}, "definition": {repr(definition)}}},')
        else:
            jargon_lines.append(f'        {{"term": {repr(str(entry))}, "definition": ""}},')
    jargon_block = "\n".join(jargon_lines) if jargon_lines else "        # No jargon terms configured"

    # Terminology lock — list of dicts with canonical/rejected
    terminology_lock_raw = tsr.get("terminology_lock", [])
    term_lock_lines = []
    for entry in terminology_lock_raw:
        if isinstance(entry, dict):
            canonical = entry.get("canonical", "")
            rejected = entry.get("rejected", [])
            if isinstance(rejected, str):
                rejected = [rejected]
            term_lock_lines.append(
                f'        {{"canonical": {repr(canonical)}, "rejected": {repr(rejected)}}},'
            )
    term_lock_block = "\n".join(term_lock_lines) if term_lock_lines else "        # No terminology lock configured"

    # Build settings
    build = audit.get("build", {})
    page_limit = build.get("page_limit", 10)
    overfull_threshold = build.get("overfull_threshold", 10)

    # Build the config header as an f-string (small, controlled)
    config_block = f'''
# ---------------------------------------------------------------------------
# Configuration (derived from project.yaml at generation time)
# ---------------------------------------------------------------------------

ABSTRACT_WORD_LIMIT = {abstract_word_limit}
MAX_PARAGRAPH_SENTENCES = {max_paragraph_sentences}
PAGE_LIMIT = {page_limit}
OVERFULL_THRESHOLD = {overfull_threshold}

JARGON_TERMS = [
{jargon_block}
    ]

TERMINOLOGY_LOCK = [
{term_lock_block}
    ]
'''

    # The rest of the script is a plain string — no f-string interpolation needed
    # We only inject project_name into the docstring via concatenation
    content = (
        '#!/usr/bin/env python3\n'
        '"""audit_report.py — Report consistency auditor.\n'
        '\n'
        'Auto-generated by gen_report_auditor.py from project.yaml.\n'
        'Re-run generator after changing project.yaml audit configuration.\n'
        '\n'
        f'Project: {project_name}\n'
        '\n'
        'Checks performed:\n'
        '  1.  Title check (Rule 1) — reject procedural title patterns\n'
        '  2.  Abstract word count (Rule 5)\n'
        '  3.  Paragraph length (Rule 3)\n'
        '  4.  Jargon first-use (Rule 2)\n'
        '  5.  Terminology consistency (Rule 4)\n'
        '  6.  Section headers declarative (Rule 7)\n'
        '  7.  Figure/table cross-refs — orphan detection\n'
        '  8.  Citation completeness — orphan detection\n'
        '  9.  No new results in Discussion (Rule 8)\n'
        '  10. Caption quality\n'
        '  11. Build check (pdflatex)\n'
        '  12. Page count\n'
        '  13. URL escaping\n'
        '\n'
        'Exit codes:\n'
        '  0 — All checks passed (no CRITICAL/HIGH findings)\n'
        '  1 — One or more CRITICAL/HIGH findings\n'
        '"""\n'
        'import argparse\n'
        'import os\n'
        'import re\n'
        'import subprocess\n'
        'import sys\n'
        'from pathlib import Path\n'
        '\n'
        + config_block +
        _SCRIPT_BODY
    )

    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(content)
    out.chmod(0o755)

    # Summary
    print(f"Generated: {output_path}")
    print(f"  Project:            {project_name}")
    print(f"  Abstract limit:     {abstract_word_limit} words")
    print(f"  Max para sentences: {max_paragraph_sentences}")
    print(f"  Page limit:         {page_limit}")
    print(f"  Jargon terms:       {len(jargon_terms_raw)}")
    print(f"  Terminology locks:  {len(terminology_lock_raw)}")
    print(f"  Checks:             13 (title, abstract, paragraphs, jargon, "
          f"terminology, headers, crossrefs, citations, discussion, "
          f"captions, build, pages, URLs)")
    return content


