def write_findings_md(findings: list[Finding], output_path: Path, report_path: Path, phase: str) -> None:
    """Write findings to FINDINGS.md."""
    lines = [
        "# Report Audit Findings",
        "",
        f"**Report:** `{report_path}`",
        f"**Phase:** {phase}",
        f"**Checks run:** {len(set(f.check for f in findings))}",
        "",
        "---",
        "",
        "## Summary",
        "",
    ]

    by_severity = {}
    for f in findings:
        by_severity.setdefault(f.severity, []).append(f)

    for sev in [CRITICAL, HIGH, MEDIUM, LOW, "PASS"]:
        count = len(by_severity.get(sev, []))
        lines.append(f"- **{sev}:** {count}")
    lines.append("")

    # Per-check verdicts
    lines.append("## Per-Check Verdicts")
    lines.append("")
    lines.append("| Check | Verdict | Details |")
    lines.append("|-------|---------|---------|")

    check_names_seen = []
    for f in findings:
        if f.check not in check_names_seen:
            check_names_seen.append(f.check)

    for check_name in check_names_seen:
        check_findings = [f for f in findings if f.check == check_name]
        worst = "PASS"
        for sev in [CRITICAL, HIGH, MEDIUM, LOW]:
            if any(f.severity == sev for f in check_findings):
                worst = sev
                break
        # Pick first non-PASS finding for details, or PASS message
        detail_f = next(
            (f for f in check_findings if f.severity != "PASS"),
            check_findings[0] if check_findings else None,
        )
        detail = detail_f.message[:100] if detail_f else ""
        verdict = "PASS" if worst == "PASS" else f"**FAIL** ({worst})"
        lines.append(f"| {check_name} | {verdict} | {detail} |")

    lines.append("")

    # Detailed findings by severity
    for sev in [CRITICAL, HIGH, MEDIUM, LOW]:
        sev_findings = by_severity.get(sev, [])
        if not sev_findings:
            continue
        lines.append(f"## {sev} Findings")
        lines.append("")
        for f in sev_findings:
            loc = f" (line {f.line})" if f.line else ""
            lines.append(f"- **[{f.check}]**{loc}: {f.message}")
        lines.append("")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines) + "\n")
    print(f"  Findings written: {output_path}")


