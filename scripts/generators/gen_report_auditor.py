def read_report(report_path: Path) -> str:
    """Read report file and return full text."""
    return report_path.read_text(encoding="utf-8", errors="replace")


def is_latex(report_path: Path) -> bool:
    return report_path.suffix.lower() in (".tex", ".latex")


def extract_title_latex(text: str) -> str | None:
    """Extract title from LaTeX \\title{...}."""
    m = re.search(r"\\title\{([^{}]+)\}", text)
    if m:
        return m.group(1).strip()
    # Handle multi-line title
    m = re.search(r"\\title\{(.+?)\}", text, re.DOTALL)
    if m:
        return " ".join(m.group(1).split()).strip()
    return None


def extract_title_md(text: str) -> str | None:
    """Extract title from first # heading in Markdown."""
    for line in text.splitlines():
        stripped = line.strip()
        if stripped.startswith("# ") and not stripped.startswith("## "):
            return stripped[2:].strip()
    return None


def extract_abstract_latex(text: str) -> str | None:
    """Extract abstract text from LaTeX."""
    m = re.search(
        r"\\begin\{abstract\}(.+?)\\end\{abstract\}",
        text, re.DOTALL,
    )
    if m:
        return m.group(1).strip()
    return None


def extract_abstract_md(text: str) -> str | None:
    """Extract abstract section from Markdown (text under ## Abstract)."""
    lines = text.splitlines()
    in_abstract = False
    abstract_lines = []
    for line in lines:
        stripped = line.strip()
        if re.match(r"^##\s+Abstract", stripped, re.IGNORECASE):
            in_abstract = True
            continue
        if in_abstract:
            if re.match(r"^##\s+", stripped):
                break
            abstract_lines.append(line)
    if abstract_lines:
        return "\n".join(abstract_lines).strip()
    return None


def get_sections_latex(text: str) -> dict[str, str]:
    """Split LaTeX into sections by \\section{...}."""
    sections = {}
    pattern = re.compile(r"\\section\{([^{}]+)\}")
    parts = pattern.split(text)
    # parts = [preamble, name1, body1, name2, body2, ...]
    for i in range(1, len(parts) - 1, 2):
        name = parts[i].strip()
        body = parts[i + 1] if i + 1 < len(parts) else ""
        sections[name] = body
    return sections


def get_sections_md(text: str) -> dict[str, str]:
    """Split Markdown into sections by ## headings."""
    sections = {}
    lines = text.splitlines()
    current_name = None
    current_lines: list[str] = []
    for line in lines:
        m = re.match(r"^##\s+(.+)", line)
        if m:
            if current_name is not None:
                sections[current_name] = "\n".join(current_lines)
            current_name = m.group(1).strip()
            current_lines = []
        else:
            current_lines.append(line)
    if current_name is not None:
        sections[current_name] = "\n".join(current_lines)
    return sections


def get_paragraphs(text: str) -> list[tuple[int, str]]:
    """Split text into paragraphs. Returns list of (approx_line, paragraph_text)."""
    paragraphs = []
    lines = text.splitlines()
    current: list[str] = []
    start_line = 1
    for i, line in enumerate(lines, start=1):
        stripped = line.strip()
        # Paragraph break: blank line or LaTeX \par
        if not stripped or stripped == "\\par":
            if current:
                paragraphs.append((start_line, " ".join(current)))
                current = []
            start_line = i + 1
        else:
            # Skip comment lines and headings
            if stripped.startswith("%") or stripped.startswith("#"):
                if current:
                    paragraphs.append((start_line, " ".join(current)))
                    current = []
                start_line = i + 1
                continue
            # Skip LaTeX commands that are structural
            if re.match(r"\\(begin|end|section|subsection|label|caption)\{", stripped):
                if current:
                    paragraphs.append((start_line, " ".join(current)))
                    current = []
                start_line = i + 1
                continue
            current.append(stripped)
    if current:
        paragraphs.append((start_line, " ".join(current)))
    return paragraphs


def count_sentences(text: str) -> int:
    """Approximate sentence count in a paragraph."""
    # Remove common abbreviations that contain dots
    cleaned = text
    for abbr in ("e.g.", "i.e.", "et al.", "Fig.", "Eq.", "vs.", "Dr.", "Mr.", "Mrs.", "Prof."):
        cleaned = cleaned.replace(abbr, abbr.replace(".", ""))
    # Count sentence-ending punctuation
    count = len(re.findall(r"[.!?]+(?:\s|$)", cleaned))
    return max(count, 1)  # at least 1 sentence if text exists


def extract_numbers_from_text(text: str) -> set[str]:
    """Extract all numeric values from text as canonical strings."""
    nums = set()
    for m in re.finditer(r"(?<!\\)\b(\d+(?:\.\d+)?)\b", text):
        nums.add(m.group(1))
    return nums


