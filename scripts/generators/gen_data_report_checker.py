def load_manifest(manifest_path: Path) -> dict:
    """Load the execution manifest.

    Expected structure:
    {{
        "mappings": [
            {{
                "artifact": "outputs/part1/metrics.csv",
                "report_section": "Results",
                "report_file": "docs/report.tex",
                "metrics": ["accuracy", "f1"]
            }},
            ...
        ]
    }}

    Falls back to scanning outputs/ if no manifest is found.
    """
    if manifest_path.exists():
        try:
            data = json.loads(manifest_path.read_text(encoding="utf-8"))
            return data
        except (json.JSONDecodeError, KeyError) as e:
            print(f"  WARNING: Could not parse manifest {{manifest_path}}: {{e}}",
                  file=sys.stderr)
    return {{}}


