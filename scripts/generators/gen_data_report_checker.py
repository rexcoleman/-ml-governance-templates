def read_report(report_path: Path) -> list[dict]:
    """Read a report file and extract all numeric claims.

    Supports .tex (LaTeX) and .md (Markdown) files.
    Returns a list of number-entry dicts.
    """
    text = report_path.read_text(encoding="utf-8", errors="replace")
    lines = text.splitlines()
    is_latex = report_path.suffix.lower() in (".tex", ".latex")

    all_numbers = []
    for i, line in enumerate(lines, start=1):
        # Skip comment lines
        if is_latex and line.strip().startswith("%"):
            continue
        if not is_latex and line.strip().startswith("<!--"):
            continue

        entries = extract_numbers_from_line(line, i, is_latex=is_latex)
        all_numbers.extend(entries)

    return all_numbers


