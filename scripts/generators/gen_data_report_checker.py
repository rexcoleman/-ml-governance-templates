def run_checks(
    report_path: Path,
    outputs_dir: Path,
    manifest_path: Path | None = None,
) -> list[dict]:
    """Run all data-vs-report consistency checks.

    Returns a list of finding dicts.
    """
    findings: list[dict] = []

    # Step 1: Load manifest
    effective_manifest = None
    if manifest_path and manifest_path.exists():
        effective_manifest = load_manifest(manifest_path)
    elif (outputs_dir / MANIFEST_FILE).exists():
        effective_manifest = load_manifest(outputs_dir / MANIFEST_FILE)
    elif Path(MANIFEST_FILE).exists():
        effective_manifest = load_manifest(Path(MANIFEST_FILE))

    # Step 2: Extract numbers from report
    if not report_path.exists():
        findings.append({{
            "type": "error",
            "severity": "FAIL",
            "message": f"Report file not found: {{report_path}}",
            "line_num": 0,
        }})
        return findings

    report_numbers = read_report(report_path)
    if not report_numbers:
        findings.append({{
            "type": "info",
            "severity": "PASS",
            "message": "No numeric claims found in report.",
            "line_num": 0,
        }})
        return findings

    print(f"  Extracted {{len(report_numbers)}} numeric values from {{report_path}}")

    # Step 3: Collect all artifact data
    all_artifact_data: dict[str, Any] = {{}}

    # From manifest mappings
    if effective_manifest and "mappings" in effective_manifest:
        for mapping in effective_manifest["mappings"]:
            artifact_rel = mapping.get("artifact", "")
            artifact_path = outputs_dir / artifact_rel
            if not artifact_path.exists():
                artifact_path = Path(artifact_rel)
            if artifact_path.exists():
                loaded = load_artifact_numbers(artifact_path)
                data = loaded.get("data", {{}})
                if isinstance(data, dict):
                    all_artifact_data.update(data)
            else:
                findings.append({{
                    "type": "missing_artifact",
                    "severity": "WARNING",
                    "message": f"Artifact not found: {{artifact_rel}}",
                    "line_num": 0,
                }})

    # Also scan outputs_dir for any CSV/JSON files not in manifest
    if outputs_dir.exists():
        for fpath in sorted(outputs_dir.rglob("*")):
            if fpath.suffix.lower() in (".csv", ".json") and fpath.is_file():
                loaded = load_artifact_numbers(fpath)
                data = loaded.get("data", {{}})
                if isinstance(data, dict):
                    # Prefix with relative path to avoid collisions
                    prefix = str(fpath.relative_to(outputs_dir)).replace(os.sep, ".")
                    prefix = prefix.rsplit(".", 1)[0]  # remove extension
                    for k, v in data.items():
                        all_artifact_data[f"{{prefix}}.{{k}}"] = v

    if not all_artifact_data:
        findings.append({{
            "type": "warning",
            "severity": "WARNING",
            "message": (
                "No artifact data found. Ensure outputs directory contains "
                "CSV/JSON files or that the manifest points to valid artifacts."
            ),
            "line_num": 0,
        }})
        return findings

    print(f"  Loaded {{len(all_artifact_data)}} data points from artifacts")

    # Step 4: Cross-reference each report number against data
    checked = 0
    matched = 0

    for entry in report_numbers:
        # Skip very common numbers that are unlikely to be metrics
        # (e.g., section numbers, years, reference numbers)
        if entry["value"] in (0, 1, 2, 3) and entry["metric"] is None:
            continue
        if 1900 <= entry["value"] <= 2100 and entry["metric"] is None:
            continue  # likely a year

        best_key, best_val = find_best_data_match(entry, all_artifact_data)

        if best_key is None or best_val is None:
            continue  # no matching data found — skip silently

        checked += 1
        tol = get_tolerance(entry.get("metric"))

        if numbers_match(entry["value"], best_val, tol):
            matched += 1
            findings.append({{
                "type": "numeric_match",
                "severity": "PASS",
                "line_num": entry["line_num"],
                "message": (
                    f"Line {{entry['line_num']}}: report={{entry['value']}} "
                    f"matches data={{best_val}} (key={{best_key}}, tol={{tol}})"
                ),
                "report_value": entry["value"],
                "data_value": best_val,
                "data_key": best_key,
                "tolerance": tol,
                "metric": entry.get("metric"),
            }})
        else:
            findings.append({{
                "type": "numeric_mismatch",
                "severity": "FAIL",
                "line_num": entry["line_num"],
                "message": (
                    f"Line {{entry['line_num']}}: report={{entry['value']}} "
                    f"!= data={{best_val}} (key={{best_key}}, tol={{tol}})"
                ),
                "context": entry["context"],
                "report_value": entry["value"],
                "data_value": best_val,
                "data_key": best_key,
                "tolerance": tol,
                "metric": entry.get("metric"),
            }})

        # Additional checks
        sign_finding = check_sign_consistency(entry, best_val)
        if sign_finding:
            findings.append(sign_finding)

        mag_finding = check_magnitude_plausibility(entry, best_val)
        if mag_finding:
            findings.append(mag_finding)

    print(f"  Cross-referenced {{checked}} values, {{matched}} matched")

    return findings


