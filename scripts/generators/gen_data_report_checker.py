def load_csv_numbers(csv_path: Path) -> dict[str, list[float]]:
    """Load all numeric columns from a CSV file.

    Returns a dict mapping column_name -> list of float values.
    """
    result: dict[str, list[float]] = {{}}
    try:
        with open(csv_path, newline="", encoding="utf-8") as f:
            reader = csv.DictReader(f)
            for row in reader:
                for col, val in row.items():
                    if col is None:
                        continue
                    parsed = parse_number(val)
                    if parsed is not None:
                        result.setdefault(col, []).append(parsed)
    except Exception as e:
        print(f"  WARNING: Could not read CSV {{csv_path}}: {{e}}", file=sys.stderr)
    return result


def load_json_numbers(json_path: Path) -> dict[str, float]:
    """Load numeric values from a JSON file (flat or one-level nested).

    Returns a dict mapping dotted-key-path -> float value.
    """
    result: dict[str, float] = {{}}
    try:
        data = json.loads(json_path.read_text(encoding="utf-8"))
        _flatten_json(data, "", result)
    except Exception as e:
        print(f"  WARNING: Could not read JSON {{json_path}}: {{e}}", file=sys.stderr)
    return result


def _flatten_json(obj: Any, prefix: str, out: dict[str, float]) -> None:
    """Recursively flatten a JSON object into dotted-key -> float pairs."""
    if isinstance(obj, dict):
        for k, v in obj.items():
            key = f"{{prefix}}.{{k}}" if prefix else k
            _flatten_json(v, key, out)
    elif isinstance(obj, list):
        for i, v in enumerate(obj):
            key = f"{{prefix}}[{{i}}]"
            _flatten_json(v, key, out)
    elif isinstance(obj, (int, float)) and not isinstance(obj, bool):
        out[prefix] = float(obj)


def load_artifact_numbers(artifact_path: Path) -> dict[str, Any]:
    """Load numeric data from an artifact file (CSV or JSON).

    Returns a dict with:
      'source': file path string
      'type': 'csv' or 'json'
      'data': the loaded data structure
    """
    suffix = artifact_path.suffix.lower()
    if suffix == ".csv":
        return {{
            "source": str(artifact_path),
            "type": "csv",
            "data": load_csv_numbers(artifact_path),
        }}
    elif suffix == ".json":
        return {{
            "source": str(artifact_path),
            "type": "json",
            "data": load_json_numbers(artifact_path),
        }}
    else:
        return {{"source": str(artifact_path), "type": "unknown", "data": {{}}}}


