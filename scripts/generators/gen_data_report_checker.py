def get_tolerance(metric: str | None) -> float:
    """Get the appropriate tolerance for a given metric name."""
    if metric is None:
        return TOLERANCES["default"]

    metric_lower = metric.lower()

    # Percentage metrics
    if any(kw in metric_lower for kw in ("percent", "rate", "ratio", "%")):
        return TOLERANCES["percent"]

    # Count metrics (integers)
    if any(kw in metric_lower for kw in ("count", "total", "num", "n_")):
        return TOLERANCES["count"]

    # General float metrics
    return TOLERANCES["float"]


def numbers_match(report_val: float, data_val: float, tolerance: float) -> bool:
    """Check if two numbers match within tolerance.

    Uses absolute tolerance for small numbers and relative tolerance for large ones.
    """
    if data_val == 0 and report_val == 0:
        return True
    if data_val == 0:
        return abs(report_val) <= tolerance

    # Relative difference
    rel_diff = abs(report_val - data_val) / max(abs(data_val), 1e-12)
    # Absolute difference
    abs_diff = abs(report_val - data_val)

    # Pass if either absolute or relative is within tolerance
    return abs_diff <= tolerance or rel_diff <= tolerance


def check_sign_consistency(entry: dict, data_val: float) -> dict | None:
    """Check that sign language in context matches the actual delta sign.

    Returns a finding dict if inconsistent, else None.
    """
    context = entry.get("context", "")
    value = entry["value"]

    # Only check if we can infer expected sign from context
    has_positive = POSITIVE_WORDS.search(context)
    has_negative = NEGATIVE_WORDS.search(context)

    if has_positive and not has_negative:
        # Context implies improvement/increase — value should be positive or data > 0
        if value < 0:
            return {{
                "type": "sign_inconsistency",
                "severity": "WARNING",
                "line_num": entry["line_num"],
                "message": (
                    f"Context suggests positive change but value is {{value}} "
                    f"(line {{entry['line_num']}})"
                ),
                "context": context,
                "report_value": value,
                "data_value": data_val,
            }}
    elif has_negative and not has_positive:
        # Context implies reduction/decrease — value should be negative or decreasing
        if value > 0 and entry.get("metric") and "loss" not in entry["metric"].lower():
            return {{
                "type": "sign_inconsistency",
                "severity": "WARNING",
                "line_num": entry["line_num"],
                "message": (
                    f"Context suggests negative change but value is {{value}} "
                    f"(line {{entry['line_num']}})"
                ),
                "context": context,
                "report_value": value,
                "data_value": data_val,
            }}

    return None


def check_magnitude_plausibility(
    entry: dict, data_val: float, factor: float = MAGNITUDE_PLAUSIBILITY_FACTOR
) -> dict | None:
    """Flag if report value differs from data value by more than `factor`x.

    Returns a finding dict if implausible, else None.
    """
    if data_val == 0 or entry["value"] == 0:
        return None

    ratio = abs(entry["value"] / data_val)
    if ratio > factor or ratio < (1.0 / factor):
        return {{
            "type": "magnitude_implausible",
            "severity": "WARNING",
            "line_num": entry["line_num"],
            "message": (
                f"Report value {{entry['value']}} differs from data {{data_val}} "
                f"by factor {{ratio:.2f}}x (threshold: {{factor}}x) "
                f"(line {{entry['line_num']}})"
            ),
            "context": entry["context"],
            "report_value": entry["value"],
            "data_value": data_val,
            "ratio": ratio,
        }}
    return None


def find_best_data_match(
    entry: dict, artifact_data: dict[str, Any]
) -> tuple[str | None, float | None]:
    """Find the best matching data value for a report number entry.

    Looks in artifact data for a key matching the entry's metric keyword,
    and returns the closest value.

    Returns (matched_key, matched_value) or (None, None).
    """
    metric = entry.get("metric")
    value = entry["value"]

    if not artifact_data:
        return None, None

    best_key = None
    best_val = None
    best_diff = float("inf")

    for key, data_val in artifact_data.items():
        # If metric keyword matches key name, prioritize it
        key_lower = key.lower()
        metric_bonus = 0
        if metric and metric.lower() in key_lower:
            metric_bonus = 1000  # large bonus for keyword match

        if isinstance(data_val, list):
            # For CSV columns: check aggregates (last value, mean, min, max)
            candidates = []
            if data_val:
                candidates.append(("last", data_val[-1]))
                candidates.append(("mean", sum(data_val) / len(data_val)))
                candidates.append(("min", min(data_val)))
                candidates.append(("max", max(data_val)))
            for label, cand in candidates:
                diff = abs(value - cand)
                score = diff - metric_bonus
                if score < best_diff:
                    best_diff = score
                    best_key = f"{{key}}.{{label}}"
                    best_val = cand
        elif isinstance(data_val, (int, float)):
            diff = abs(value - data_val)
            score = diff - metric_bonus
            if score < best_diff:
                best_diff = score
                best_key = key
                best_val = data_val

    return best_key, best_val


