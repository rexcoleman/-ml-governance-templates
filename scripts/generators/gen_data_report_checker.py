def write_findings_json(findings: list[dict], output_path: Path) -> None:
    """Write findings to a structured JSON file."""
    summary = {{
        "total": len(findings),
        "pass": sum(1 for f in findings if f["severity"] == "PASS"),
        "fail": sum(1 for f in findings if f["severity"] == "FAIL"),
        "warning": sum(1 for f in findings if f["severity"] == "WARNING"),
    }}
    report = {{
        "summary": summary,
        "findings": findings,
    }}
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(report, indent=2, default=str) + "\\n")
    print(f"  JSON findings written: {{output_path}}")


def print_human_summary(findings: list[dict]) -> None:
    """Print a human-readable summary of findings."""
    passes = [f for f in findings if f["severity"] == "PASS"]
    fails = [f for f in findings if f["severity"] == "FAIL"]
    warnings = [f for f in findings if f["severity"] == "WARNING"]

    print()
    print("=" * 70)
    print("DATA vs REPORT CONSISTENCY CHECK")
    print("=" * 70)
    print(f"  PASS:    {{len(passes)}}")
    print(f"  FAIL:    {{len(fails)}}")
    print(f"  WARNING: {{len(warnings)}}")
    print("-" * 70)

    if fails:
        print()
        print("FAILURES:")
        for f in fails:
            print(f"  [FAIL] {{f['message']}}")
            if "context" in f:
                ctx = f["context"][:120]
                print(f"         Context: {{ctx}}")
        print()

    if warnings:
        print("WARNINGS:")
        for w in warnings:
            print(f"  [WARN] {{w['message']}}")
        print()

    if not fails and not warnings:
        print("  All numeric claims consistent with source data.")
    print("=" * 70)


