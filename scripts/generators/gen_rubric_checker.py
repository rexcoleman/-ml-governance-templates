def run_checks(
    report_path: Path,
    rubric_source: Path,
    faq_source: Path,
    traceability_path: Optional[Path] = None,
) -> tuple[list[dict], list[dict], list[str]]:
    """Run all rubric/FAQ coverage checks.

    Returns (rubric_results, faq_results, gap_pattern_warnings).
    """
    # Step 1: Extract rubric items
    rubric_items = []
    if rubric_source.exists():
        rubric_text = rubric_source.read_text(encoding="utf-8", errors="replace")
        rubric_items = extract_rubric_items(rubric_text)
        print(f"  Extracted {{len(rubric_items)}} rubric items from {{rubric_source}}")
    else:
        print(f"  WARNING: Rubric source not found: {{rubric_source}}", file=sys.stderr)

    # Step 2: Extract FAQ items
    faq_items = []
    if faq_source.exists():
        faq_text = faq_source.read_text(encoding="utf-8", errors="replace")
        faq_items = extract_faq_items(faq_text)
        print(f"  Extracted {{len(faq_items)}} FAQ items from {{faq_source}}")
    else:
        print(f"  WARNING: FAQ source not found: {{faq_source}}", file=sys.stderr)

    # Step 3: Load traceability matrix if provided
    traceability: dict[str, dict] = {{}}
    if traceability_path and traceability_path.exists():
        trace_text = traceability_path.read_text(encoding="utf-8", errors="replace")
        traceability = parse_traceability(trace_text)
        print(f"  Loaded {{len(traceability)}} traceability mappings from {{traceability_path}}")

    # Step 4: Read and parse report
    if not report_path.exists():
        print(f"  ERROR: Report file not found: {{report_path}}", file=sys.stderr)
        # Return everything as GAP
        rubric_results = [
            {{"id": it["id"], "text": it["text"], "status": "GAP",
             "section": "", "score": 0.0, "details": "Report file not found",
             "is_critical": it.get("is_critical", False),
             "category": it.get("category", "")}}
            for it in rubric_items
        ]
        faq_results = [
            {{"id": it["id"], "text": it["text"], "status": "GAP",
             "section": "", "score": 0.0, "details": "Report file not found",
             "is_critical": False, "category": ""}}
            for it in faq_items
        ]
        return rubric_results, faq_results, []

    sections = read_report_sections(report_path)
    print(f"  Parsed {{len(sections) - 1}} sections from {{report_path}}")

    # Step 5: Check each rubric item
    rubric_results = []
    for item in rubric_items:
        result = check_item_coverage(item, sections, traceability)
        rubric_results.append(result)

    # Step 6: Check each FAQ item
    faq_results = []
    for item in faq_items:
        result = check_item_coverage(item, sections, traceability)
        faq_results.append(result)

    # Step 7: Check common gap patterns against full report text
    full_text = sections.get("_full", "")
    gap_warnings = check_gap_patterns(full_text)

    return rubric_results, faq_results, gap_warnings


