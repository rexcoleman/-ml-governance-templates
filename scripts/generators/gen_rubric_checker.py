def read_report_sections(report_path: Path) -> dict[str, str]:
    """Read a report file and split it into sections.

    Returns dict mapping section_name -> section_text.
    Also includes a special "_full" key with the entire text.
    """
    text = report_path.read_text(encoding="utf-8", errors="replace")
    sections: dict[str, str] = {{"_full": text}}

    is_latex = report_path.suffix.lower() in (".tex", ".latex")

    if is_latex:
        # Split on LaTeX section commands
        sec_re = re.compile(
            r"\\\\(?:section|subsection|subsubsection)\\{{([^{{}}]+)\\}}",
        )
    else:
        # Split on Markdown headers
        sec_re = re.compile(r"^(#{{1,4}})\\s+(.+)$", re.MULTILINE)

    if is_latex:
        parts = sec_re.split(text)
        # parts: [preamble, sec1_name, sec1_text, sec2_name, sec2_text, ...]
        current_name = "_preamble"
        current_text = ""
        i = 0
        while i < len(parts):
            if sec_re.match(parts[i]) if i > 0 else False:
                sections[current_name] = current_text
                current_name = parts[i]
                current_text = ""
            else:
                current_text += parts[i]
            i += 1
        sections[current_name] = current_text

        # Re-parse more carefully
        sections = {{"_full": text}}
        matches = list(sec_re.finditer(text))
        for idx, match in enumerate(matches):
            name = match.group(1).strip()
            start = match.end()
            end = matches[idx + 1].start() if idx + 1 < len(matches) else len(text)
            sections[name] = text[start:end]
    else:
        matches = list(sec_re.finditer(text))
        for idx, match in enumerate(matches):
            name = match.group(2).strip()
            start = match.end()
            end = matches[idx + 1].start() if idx + 1 < len(matches) else len(text)
            sections[name] = text[start:end]

    return sections


