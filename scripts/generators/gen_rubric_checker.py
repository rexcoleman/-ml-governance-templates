def compute_keyword_score(requirement_text: str, section_text: str) -> float:
    """Compute a keyword overlap score between a requirement and section text.

    Returns a float between 0.0 and 1.0.
    """
    # Extract significant words from the requirement
    req_words = set(
        w.lower() for w in re.findall(r"\\b[a-zA-Z]{{3,}}\\b", requirement_text)
    )
    # Remove very common words
    stopwords = {{
        "the", "and", "for", "are", "but", "not", "you", "all", "can",
        "had", "her", "was", "one", "our", "out", "has", "have", "been",
        "that", "this", "with", "from", "they", "will", "what", "when",
        "make", "like", "each", "does", "how", "its", "may", "than",
        "use", "used", "using", "your", "should", "must", "also",
        "which", "their", "would", "about", "into", "more", "some",
    }}
    req_words -= stopwords

    if not req_words:
        return 0.0

    section_lower = section_text.lower()
    matched = sum(1 for w in req_words if w in section_lower)

    return matched / len(req_words)


def check_gap_patterns(section_text: str) -> list[str]:
    """Check which common gap patterns are missing from a section.

    Returns list of gap pattern descriptions that are NOT found.
    """
    missing = []
    section_lower = section_text.lower()

    pattern_checks = [
        ("distance metric justification",
         r"(?:distance|similarity)\\s+metric.*(?:because|since|chosen|select|justif)"),
        ("similarity metric justification",
         r"(?:distance|similarity).*(?:reason|rationale|motivation|why)"),
        ("hyperparameter search range",
         r"(?:hyperparameter|hyper-parameter|hp).*(?:range|search|grid|sweep|tuning)"),
        ("hyperparameter sensitivity",
         r"(?:hyperparameter|hyper-parameter|hp).*(?:sensitiv|impact|effect|vary)"),
        ("initialization choice",
         r"(?:initial|seed|start|q.?0|bootstrap).*(?:choice|select|set|value)"),
        ("convergence criteria",
         r"(?:converg|stop|terminat).*(?:criteri|threshold|condition|when)"),
        ("reward function details",
         r"(?:reward|objective)\\s+(?:function|signal|formul)"),
        ("ablation analysis",
         r"(?:ablat|remov|without|drop).*(?:analys|study|experiment|result)"),
        ("noise sensitivity",
         r"(?:noise|perturb|robust).*(?:sensitiv|analys|test|effect)"),
        ("suggested improvements",
         r"(?:future|improv|suggest|recommend|next|limit).*(?:work|step|direction|could)"),
    ]

    for name, pattern in pattern_checks:
        if not re.search(pattern, section_lower):
            missing.append(name)

    return missing


def check_item_coverage(
    item: dict,
    sections: dict[str, str],
    traceability: dict[str, dict],
) -> dict:
    """Check whether a single rubric/FAQ item is covered in the report.

    Returns a result dict with: id, text, status, section, score, details.
    """
    item_id = item["id"]
    item_text = item["text"]

    # If traceability matrix has a mapping, use it
    if item_id in traceability:
        mapped = traceability[item_id]
        if mapped["status"] in ("ADDRESSED", "PARTIAL", "GAP", "N/A"):
            mapped_section = mapped["section"]
            # Verify the mapped section actually exists and has content
            if mapped_section and mapped_section in sections:
                score = compute_keyword_score(item_text, sections[mapped_section])
                if score >= 0.4:
                    return {{
                        "id": item_id,
                        "text": item_text,
                        "status": "ADDRESSED",
                        "section": mapped_section,
                        "score": score,
                        "details": f"Traceability-mapped, keyword score {{score:.2f}}",
                        "is_critical": item.get("is_critical", False),
                        "category": item.get("category", ""),
                    }}
                elif score >= 0.2:
                    return {{
                        "id": item_id,
                        "text": item_text,
                        "status": "PARTIAL",
                        "section": mapped_section,
                        "score": score,
                        "details": f"Traceability-mapped but low keyword overlap ({{score:.2f}})",
                        "is_critical": item.get("is_critical", False),
                        "category": item.get("category", ""),
                    }}
            # If mapped section not found or empty, fall through to keyword search

    # Keyword-based search across all sections
    best_section = None
    best_score = 0.0

    for sec_name, sec_text in sections.items():
        if sec_name == "_full":
            continue
        score = compute_keyword_score(item_text, sec_text)
        if score > best_score:
            best_score = score
            best_section = sec_name

    # If no section matched well, try the full text
    if best_score < 0.2:
        full_score = compute_keyword_score(item_text, sections.get("_full", ""))
        if full_score > best_score:
            best_score = full_score
            best_section = "(full text search)"

    # Determine status based on score
    if best_score >= 0.5:
        status = "ADDRESSED"
    elif best_score >= 0.25:
        status = "PARTIAL"
    else:
        status = "GAP"

    details = f"Best keyword score {{best_score:.2f}} in '{{best_section or 'none'}}'"

    return {{
        "id": item_id,
        "text": item_text,
        "status": status,
        "section": best_section or "",
        "score": best_score,
        "details": details,
        "is_critical": item.get("is_critical", False),
        "category": item.get("category", ""),
    }}


